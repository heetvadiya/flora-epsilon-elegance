const TableRow = React.forwardRef<
  HTMLTableRowElement,
  React.HTMLAttributes<HTMLTableRowElement>
>(({ className, ...props }, ref) => {
  const [isVisible, setIsVisible] = React.useState(false);
  const rowRef = React.useRef<HTMLTableRowElement>(null);

  React.useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
        }
      },
      { threshold: 0.1 }
    );

    if (rowRef.current) {
      observer.observe(rowRef.current);
    }

    return () => observer.disconnect();
  }, []);

  return (
    <tr
      ref={(node) => {
        rowRef.current = node;
        if (typeof ref === 'function') ref(node);
        else if (ref) ref.current = node;
      }}
      className={cn(
        "border-b transition-all duration-500 ease-out",
        "hover:bg-muted/50 hover:shadow-lg hover:scale-[1.01] hover:border-primary/30",
        "data-[state=selected]:bg-muted",
        "table-hover-glow",
        isVisible ? "opacity-100 translate-x-0 table-row-slide animate-in" : "opacity-0 translate-x-4",
        className
      )}
      style={{
        animationDelay: `${Math.random() * 200}ms`
      }}
      {...props}
    />
  );
})
TableRow.displayName = "TableRow"

